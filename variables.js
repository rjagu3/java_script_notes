// let will not allow re-declaration of variables
let a=10
console.log(a)

//to know data type use typeof()

console.log(typeof(a))

//var can allow to re-declare the variables
var a=20
console.log(a)

var a=30
console.log(a)

//const used to define constant variable which values cannot be modified during the program

const b=20
console.log(b)
